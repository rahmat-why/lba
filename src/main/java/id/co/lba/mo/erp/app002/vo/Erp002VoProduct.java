package id.co.lba.mo.erp.app002.vo;

public class Erp002VoProduct {
    private String productId;
    private String name;
    private String image;
    private String weight;
    private String unit;
    private String priceBuy;
    private String priceUnitDelivery;

    public String getProductId() {
        return productId;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getPriceBuy() {
        return priceBuy;
    }

    public void setPriceBuy(String priceBuy) {
        this.priceBuy = priceBuy;
    }

    public String getPriceUnitDelivery() {
        return priceUnitDelivery;
    }

    public void setPriceUnitDelivery(String priceUnitDelivery) {
        this.priceUnitDelivery = priceUnitDelivery;
    }
}
